const oracledb = require('oracledb');
const { withOracleConnection } = require('../config/oracle');

function mapSexToId(sex) {
  if (!sex) return null;
  const normalized = String(sex).trim().toLowerCase();
  if (normalized === 'male' || normalized === 'm') return 1;
  if (normalized === 'female' || normalized === 'f') return 2;
  return null;
}

function toOracleDateLiteral(dateKey) {
  // Controllers normalize to YYYY-MM-DD; enforce again at repository edge.
  const value = String(dateKey || '').trim();
  if (!/^\d{4}-\d{2}-\d{2}$/.test(value)) {
    const error = new Error('Invalid birthday format. Expected YYYY-MM-DD.');
    error.statusCode = 400;
    throw error;
  }
  return value;
}

async function createStudentOracle(payload) {
  return withOracleConnection(async (connection) => {
    // For Oracle schema parity, student ID can be provided or auto-generated by trigger.
    const desiredId = Number(payload.studentId);
    const hasNumericId = Number.isFinite(desiredId);
    const birthdayLiteral = toOracleDateLiteral(payload.birthday);

    let insertedId;
    if (hasNumericId) {
      await connection.execute(
        `
        INSERT INTO studentTb (
          stud_id, first_name, last_name, nickname, birthday, sex_id, barangay_id, device_origin, tmp_local_id
        ) VALUES (
          :studId, :firstName, :lastName, :nickname, TO_DATE(:birthday, 'YYYY-MM-DD'), :sexId, 1, :deviceOrigin, :tmpLocalId
        )
        `,
        {
          studId: desiredId,
          firstName: payload.firstName,
          lastName: payload.lastName,
          nickname: payload.nickname || payload.firstName,
          birthday: birthdayLiteral,
          sexId: mapSexToId(payload.sex),
          deviceOrigin: payload.deviceOrigin || 'API',
          tmpLocalId: payload.tmpLocalId || null,
        },
        { autoCommit: true }
      );
      insertedId = desiredId;
    } else {
      const result = await connection.execute(
        `
        INSERT INTO studentTb (
          first_name, last_name, nickname, birthday, sex_id, barangay_id, device_origin, tmp_local_id
        ) VALUES (
          :firstName, :lastName, :nickname, TO_DATE(:birthday, 'YYYY-MM-DD'), :sexId, 1, :deviceOrigin, :tmpLocalId
        ) RETURNING stud_id INTO :newStudId
        `,
        {
          firstName: payload.firstName,
          lastName: payload.lastName,
          nickname: payload.nickname || payload.firstName,
          birthday: birthdayLiteral,
          sexId: mapSexToId(payload.sex),
          deviceOrigin: payload.deviceOrigin || 'API',
          tmpLocalId: payload.tmpLocalId || null,
          newStudId: { dir: oracledb.BIND_OUT, type: oracledb.NUMBER },
        },
        { autoCommit: true }
      );
      insertedId = result.outBinds.newStudId[0];
    }

    return findStudentByIdOracle(String(insertedId));
  });
}

async function findStudentByIdOracle(studentId) {
  return withOracleConnection(async (connection) => {
    const numericId = Number(studentId);
    if (!Number.isFinite(numericId)) {
      return null;
    }

    const result = await connection.execute(
      `
      SELECT TO_CHAR(s.stud_id) AS "studentId",
             first_name AS "firstName",
             last_name AS "lastName",
             nickname AS "nickname",
             b.barangay_nm AS "area",
             TO_CHAR(s.birthday, 'YYYY-MM-DD') AS "birthday",
             TO_CHAR(s.birthday, 'YYYY-MM-DD') AS "birthdayKey",
             x.sex AS "sex",
             TO_CHAR(s.created_at, 'YYYY-MM-DD"T"HH24:MI:SS') AS "createdAt"
      FROM studentTb s
      LEFT JOIN barangayTb b ON b.barangay_id = s.barangay_id
      LEFT JOIN sexTb x ON x.sex_id = s.sex_id
      WHERE s.stud_id = :studentId
      `,
      { studentId: numericId },
      { outFormat: oracledb.OUT_FORMAT_OBJECT }
    );

    return (result.rows || [])[0] || null;
  });
}

async function listStudentsOracle() {
  return withOracleConnection(async (connection) => {
    const result = await connection.execute(
      `
      SELECT TO_CHAR(s.stud_id) AS "studentId",
             s.first_name AS "firstName",
             s.last_name AS "lastName",
             s.nickname AS "nickname",
             b.barangay_nm AS "area",
             TO_CHAR(s.birthday, 'YYYY-MM-DD') AS "birthday",
             x.sex AS "sex",
             TO_CHAR(s.created_at, 'YYYY-MM-DD"T"HH24:MI:SS') AS "createdAt"
      FROM studentTb s
      LEFT JOIN barangayTb b ON b.barangay_id = s.barangay_id
      LEFT JOIN sexTb x ON x.sex_id = s.sex_id
      ORDER BY s.created_at DESC
      `,
      {},
      { outFormat: oracledb.OUT_FORMAT_OBJECT }
    );

    return result.rows || [];
  });
}

async function leaderboardOracle() {
  return withOracleConnection(async (connection) => {
    const result = await connection.execute(
      `
      SELECT * FROM (
        SELECT TO_CHAR(s.stud_id) AS "studentId",
               s.first_name AS "firstName",
               s.last_name AS "lastName",
               NVL(SUM(sc.score), 0) AS "totalScore"
        FROM studentTb s
        LEFT JOIN scoreTb sc ON sc.stud_id = s.stud_id
        GROUP BY s.stud_id, s.first_name, s.last_name
        ORDER BY NVL(SUM(sc.score), 0) DESC, s.first_name ASC
      ) WHERE ROWNUM <= 20
      `,
      {},
      { outFormat: oracledb.OUT_FORMAT_OBJECT }
    );

    return result.rows || [];
  });
}

module.exports = {
  createStudentOracle,
  findStudentByIdOracle,
  listStudentsOracle,
  leaderboardOracle,
};
