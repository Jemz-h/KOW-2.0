-- Comprehensive Audit Logging Triggers for UPDATE operations (non-soft-delete updates)
CREATE OR REPLACE TRIGGER trg_users_update_audit
AFTER UPDATE ON Users
FOR EACH ROW
WHEN (OLD.isDeleted = NEW.isDeleted OR (OLD.isDeleted = 0 AND NEW.isDeleted = 0))
BEGIN
    -- Only log non-soft-delete updates
    IF (OLD.isDeleted = NEW.isDeleted) THEN
        INSERT INTO Audit_Log (tableName, recordID, action, changedBy, oldValues, newValues)
        VALUES ('Users', :NEW.userID, 'UPDATE', :NEW.userID,
                'firstName: ' || :OLD.firstName || ', lastName: ' || :OLD.lastName || ', gender: ' || :OLD.gender,
                'firstName: ' || :NEW.firstName || ', lastName: ' || :NEW.lastName || ', gender: ' || :NEW.gender);
    END IF;
END;
/

